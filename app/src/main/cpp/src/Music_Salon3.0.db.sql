	CONSTRAINT "id_trade" PRIMARY KEY("id_trade"),
	CONSTRAINT "trades_operations" FOREIGN KEY("id_operation") REFERENCES "operations"("id_operation")
);
CREATE TABLE IF NOT EXISTS "compositions" (
