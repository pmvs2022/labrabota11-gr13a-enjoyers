CREATE TABLE IF NOT EXISTS "trades" (
	"id_trade"	integer NOT NULL,
	"id_operation"	integer NOT NULL,
	"copies_amount"	integer NOT NULL CHECK("copies_amount" > 0),
	"trade_date"	date NOT NULL,
	"id_storageSlot"	integer NOT NULL,
